DROP DATABASE IF EXISTS TESTAIRLINE;
CREATE DATABASE IF NOT EXISTS TESTAIRLINE; 
USE TESTAIRLINE;


DROP TABLE IF EXISTS User;
CREATE TABLE User (
	userID			integer not null auto_increment,
    fName			varchar(100),
    lName			varchar(100),
    email           varchar(100),
    password        varchar(100),
    address			varchar(100),
    city            varchar(100),
    province        varchar(100),
    postal          varchar(100),
    country         varchar(100),
    phoneNumber 	varchar(100),
    companionPass  	boolean,
    loungePass 		boolean,
    joinedOnDate 	date,
    role		    varchar(100),
	primary key (userID)
);

DROP TABLE IF EXISTS Crew;
Create TABLE Crew(
                     crewID          integer not null auto_increment,
                     userID          integer,
                     position        varchar(100),
                     primary key (crewID),
                     foreign key (userID) references User(UserID)
);


DROP TABLE IF EXISTS Aircraft;
CREATE TABLE Aircraft (
	aircraftID 			integer not null auto_increment,
    rowNums 			integer,
    colNums				integer, #all planes will be 2 column so colNums refers to number of seats per col
    capacity			integer,
    name				varchar(100),
    type				varchar(100),
    pilotNum            integer,
    crewNum				integer,
    numBusinessRows		integer,
    cycles              int,
    hours               double,

    primary key (aircraftID)
);

DROP TABLE IF EXISTS Flight;
CREATE TABLE Flight (
	flightID			integer not null auto_increment,
    flightNumber		varchar(100),
    origin				varchar(100),
    destination			varchar(100),
    departureDateTime	datetime,
    arrivalDateTime		datetime,
    aircraftID			integer not null,
    coachSeatPrice		real,
    businessSeatPrice	real,
    primary key (flightID),
    foreign key (aircraftID) references Aircraft(aircraftID)
);

DROP TABLE IF EXISTS Booking;
CREATE TABLE Booking (
	bookingID			integer not null auto_increment,
    userID				integer not null,
    flightID			integer not null,
    cancelInsurance		boolean,
    paid				boolean, 
    payMethod			varchar(50),
    class				varchar(50),
    seatRow				integer,
    seatCol				varchar(1),
    passengerName		varchar(100),
    primary key (bookingID),
    foreign key (userID) references User(userID),
    foreign key (flightID) references Flight(flightID),
    CONSTRAINT UC_Booking UNIQUE (flightID, seatRow, seatCol)
);

DROP TABLE IF EXISTS CrewAssignment;
CREATE TABLE CrewAssignment (
    CrewAssignmentID integer not null auto_increment,
	crewID			integer not null,
    flightID		integer not null,
    primary key (CrewAssignmentID),
    foreign key (crewId) references Crew(CrewID),
    foreign key (flightID) references Flight(flightID)
);

